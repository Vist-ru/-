import math  # Импортируем библиотеку math для математических операций

# Функция для сложения двух чисел
def add(x, y):
    return x + y  # Возвращаем сумму x и y

# Функция для вычитания y из x
def subtract(x, y):
    return x - y  # Возвращаем результат вычитания y из x

# Функция для умножения двух чисел
def multiply(x, y):
    return x * y  # Возвращаем произведение x и y

# Функция для деления x на y
def divide(x, y):
    # Проверяем, не равен ли делитель нулю
    if y == 0:
        return "Ошибка: деление на ноль!"  # Возвращаем сообщение об ошибке при делении на ноль
    return x / y  # Возвращаем результат деления x на y

# Функция для извлечения квадратного корня из x
def square_root(x):
    # Проверяем, не является ли x отрицательным
    if x < 0:
        return "Ошибка: извлечение квадратного корня из отрицательного числа!"  # Сообщение об ошибке
    return math.sqrt(x)  # Возвращаем квадратный корень из x

# Функция для возведения x в степень y
def power(x, y):
    return x ** y  # Возвращаем результат возведения x в степень y

# Функция для вычисления тригонометрических функций (sin, cos, tan)
def trigonometric_function(func, angle_in_degrees):
    # Преобразуем угол из градусов в радианы
    angle_in_radians = math.radians(angle_in_degrees)
    # Определяем, какая тригонометрическая функция используется
    if func == 'sin':
        return math.sin(angle_in_radians)  # Возвращаем синус угла
    elif func == 'cos':
        return math.cos(angle_in_radians)  # Возвращаем косинус угла
    elif func == 'tan':
        return math.tan(angle_in_radians)  # Возвращаем тангенс угла
    else:
        return "Ошибка: неподдерживаемая функция!"  # Сообщение об ошибке для неподдерживаемых функций

# Основная калькуляторная функция
def calculator():
    print("Выберите операцию:")  # Инструкция для пользователя
    print("1. Сложение")  # Опция для сложения
    print("2. Вычитание")  # Опция для вычитания
    print("3. Умножение")  # Опция для умножения
    print("4. Деление")  # Опция для деления
    print("5. Квадратный корень")  # Опция для извлечения квадратного корня
    print("6. Возведение в степень")  # Опция для возведения в степень
    print("7. Тригонометрическая функция (sin, cos, tan)")  # Опция для тригонометрических функций
    
    choice = input("Введите номер операции: ")  # Ввод выбора операции

    # Проверяем, какие операции требуют два числа
    if choice in {'1', '2', '3', '4', '6'}:
        num1 = float(input("Введите первое число: "))  # Ввод первого числа
        num2 = float(input("Введите второе число: "))  # Ввод второго числа

        # Выполняем соответствующую арифметическую операцию в зависимости от выбора
        if choice == '1':
            print(f"Результат: {add(num1, num2)}")  # Печатаем результат сложения
        elif choice == '2':
            print(f"Результат: {subtract(num1, num2)}")  # Печатаем результат вычитания
        elif choice == '3':
            print(f"Результат: {multiply(num1, num2)}")  # Печатаем результат умножения
        elif choice == '4':
            print(f"Результат: {divide(num1, num2)}")  # Печатаем результат деления
        elif choice == '6':
            print(f"Результат: {power(num1, num2)}")  # Печатаем результат возведения в степень

    # Обрабатываем запрос на квадратный корень
    elif choice == '5':
        num = float(input("Введите число: "))  # Ввод числа для извлечения квадратного корня
        print(f"Результат: {square_root(num)}")  # Печатаем результат

    # Обрабатываем запрос на тригонометрические функции
    elif choice == '7':
        func = input("Введите тригонометрическую функцию (sin, cos, tan): ")  # Ввод функции
        angle = float(input("Введите угол в градусах: "))  # Ввод угла
        print(f"Результат: {trigonometric_function(func, angle)}")  # Печатаем результат вычисления функции

    else:
        print("Ошибка: Неподдерживаемая операция!")  # Сообщение об ошибке для неподдерживаемых операций

# Запускаем калькулятор, если файл выполняется как главная программа
if __name__ == "__main__":
    calculator()  # Вызов функции калькулятора
