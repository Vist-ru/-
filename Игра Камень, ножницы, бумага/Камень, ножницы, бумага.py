import random

def game():
    # **Варианты выбора для игры**
    choices = ["камень", "ножницы", "бумага"]  # Список возможных выборов игрока и компьютера
    
    # **Попросить игрока сделать выбор**
    user_choice = input("Выберите (камень, ножницы, бумага): ").lower()  # Получаем ввод от пользователя
    
    # **Проверка на корректность выбора пользователя**
    if user_choice not in choices:  # Если выбор не в списке допустимых
        print("Неверный выбор, попробуйте еще раз.")  # Сообщаем об ошибке
        return  # Завершаем выполнение функции

    # **Компьютерный выбор**
    computer_choice = random.choice(choices)  # Компьютер случайно выбирает один из вариантов
    print(f"Компьютер выбрал: {computer_choice}")  # Показываем выбор компьютера
    
    # **Определение победителя**
    # Если выборы совпадают, объявляем ничью
    if user_choice == computer_choice:
        print("Ничья!")  # Если оба выбрали одно и то же, объявляем ничью
    # Проверяем, победил ли игрок
    elif (user_choice == "камень" and computer_choice == "ножницы") or \
         (user_choice == "ножницы" and computer_choice == "бумага") or \
         (user_choice == "бумага" and computer_choice == "камень"):
        print("Вы победили!")  # Игрок выигрывает в этих случаях
    else:
        print("Компьютер победил!")  # Если ни одно из условий не выполнено, компьютер победил

# **Запуск игры**
if __name__ == "__main__":  # Проверяем, является ли данный модуль основным
    game()  # Запускаем игру