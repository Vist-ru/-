import random
# Генерация случайного числа от 1 до 100
secret_number = random.randint(1, 100)  # Случайное число для угадывания
attempts = 0  # Счетчик попыток
max_attempts = 10  # Максимальное количество попыток
# Приветственное сообщение для игрока
print("Добро пожаловать в игру 'Угадай число'!")
print("Я загадал число от 1 до 100. У тебя есть 10 попыток, чтобы угадать его!")
# Цикл для попыток угадывания
while attempts < max_attempts:  
    # Запрос ввода от игрока
    try:
        guess = int(input(f"Попытка {attempts + 1}: Введите ваше число: "))  # Получение числа от игрока
        attempts += 1  # Увеличиваем счетчик попыток на 1
        # Проверка введенного числа
        if guess < secret_number:
            print("Слишком низко! Попробуйте еще раз.")  # Если число ниже загаданного
        elif guess > secret_number:
            print("Слишком высоко! Попробуйте еще раз.")  # Если число выше загаданного
        else:
            # Если игрок угадал число
            print(f"Поздравляю! Вы угадали число {secret_number} за {attempts} попыток!")
            break  # Завершаем игру
    except ValueError:
        # Обработка исключений: если введенное значение не является целым числом
        print("Пожалуйста, введите целое число.")  # Сообщение об ошибке
# Проверка, исчерпал ли игрок все попытки
if attempts == max_attempts:  
    print(f"К сожалению, вы исчерпали все попытки. Загаданное число было {secret_number}.")  # Итоговое сообщение